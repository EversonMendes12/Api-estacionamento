    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
         VeiculosDAO vc = new VeiculosDAO();
            Veiculo v = new Veiculo();
                
            v.setCpf(txtCpf.getText());
            v.setPlaca(txtPlaca.getText());
            v.setCor(txtCor.getText());
            v.setCombustivel(cmbCombustivel.getSelectedItem().toString());
        
            if (rbCarro.isSelected()) {
                v.setTipo("Carro");
            }if (rbMoto.isSelected()){
                v.setTipo("Moto");
            }
            
        int posicaoSelecionada = cmbEstacionamento.getSelectedIndex();
        Estacionamento m = listaDeEstacionamento.get(posicaoSelecionada);
        v.setEstacionamento(m);
        
        v.setEstacionamento((Estacionamento) cmbEstacionamento.getSelectedItem() );
        
        int posicaoSelecionadaa = cmbMarcaCarros.getSelectedIndex();
        Marcacarro mc = listaDeCarros.get(posicaoSelecionadaa);
        v.setMarcaCarro(mc);
        
        v.setMarcaCarro((Marcacarro) cmbMarcaCarros.getSelectedItem() );
        
        int posicaoSelecionadaaa = cmbMarcaMotos.getSelectedIndex();
        MarcaMoto mm = listaDeMotos.get(posicaoSelecionadaaa);
        v.setMarcaMoto(mm);
        
        v.setMarcaMoto((MarcaMoto) cmbMarcaMotos.getSelectedItem() );
    
        vc.cadastrar(v);
    }//GEN-LAST:event_btnSalvarActionPerformed


