     public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jtableVeiculos.getModel();
        modelo.setNumRows(0);
        VeiculosDAO pdao = new VeiculosDAO();

        for (Veiculo p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getCpf(),
                p.getModelo(),
                p.getPlaca(),
                p.getCor(),
                p.getTipo(),
            });

        }

    }
    
    
