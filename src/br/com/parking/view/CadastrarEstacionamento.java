    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTableEstacionamento.getModel();
        modelo.setNumRows(0);
        EstacionamentoDAO pdao = new EstacionamentoDAO();

        for (Estacionamento p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getNomeEstacionamento(),
                p.getUf(),
                p.getCidade(),
                p.getEndereco(),
                p.getNumero(),
            });
        }
    }

