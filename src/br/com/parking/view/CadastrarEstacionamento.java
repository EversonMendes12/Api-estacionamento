    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        Estacionamento x = new Estacionamento();
        EstacionamentoDAO c = new EstacionamentoDAO();
            x.setUf(cmbUF.getSelectedItem().toString());
            x.setCidade(txtCidade.getText());
            x.setEndereco(txtEndereco.getText());
            x.setNumero(txtNumero.getText());
            x.setNomeEstacionamento(txtNome.getText());
            
            c.cadastrar(x);
            readJTable();            
            
            txtCidade.setText("");
            txtEndereco.setText("");
            txtNumero.setText("");
            txtNome.setText("");
            
            String mensagem;
            mensagem = c.getMensagem();
            
            JOptionPane.showMessageDialog(this, mensagem);
            
            
    }//GEN-LAST:event_btnSalvarActionPerformed

